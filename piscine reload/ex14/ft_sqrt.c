int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb >= 0)
		while (i * i <= nb)
		{
			if (i * i == nb)
				return (i);
			if (i * i < nb && (i + 1) * (i + 1) > nb)
				return (0);
			i++;
		}
	return (0);
}
