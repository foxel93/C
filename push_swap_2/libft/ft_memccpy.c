#include "libft.h"

void				*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t			i;
	unsigned char	*cdst;
	unsigned char	*csrc;

	cdst = (unsigned char *)dst;
	csrc = (unsigned char *)src;
	i = 0;
	while (i < n)
	{
		if ((*(cdst++) = *(csrc++)) == (unsigned char)c)
			return (cdst);
		i++;
	}
	return (NULL);
}
