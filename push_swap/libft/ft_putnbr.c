#include "libft.h"

void	ft_putnbr(int n)
{
	long a;
	long l;

	a = (long)n;
	if (a == 0)
		ft_putchar('0');
	else if (a < 0)
	{
		ft_putchar('-');
		a = -a;
	}
	l = 1;
	while (l <= a)
		l = l * 10;
	l /= 10;
	while (l)
	{
		ft_putchar((a / l) + '0');
		a %= l;
		l /= 10;
	}
}
