int		ft_strlcmp(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	while (*s1 == *s2 && *s1)
	{
		s1++;
		s2++;
		i++;
	}
	if (*s2 == '\0')
		return (i);
	return (0);
}
