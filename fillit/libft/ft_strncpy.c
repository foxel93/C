#include "libft.h"

char		*ft_strncpy(char *dst, const char *src, size_t n)
{
	char	*tmp;

	tmp = dst;
	while (*src && n > 0)
	{
		*(dst++) = *(src++);
		n--;
	}
	while (n > 0)
	{
		*(dst++) = '\0';
		n--;
	}
	return (tmp);
}
